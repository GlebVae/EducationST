public class User {
    private String fname;
    public String getFname(){
        return fname;
    }
    public void setFname (String f){
        fname = f;
    }

    private String sname;
    public String getSname(){
        return sname;
    }
    public void setSname (String s){
        sname = s;
    }

    private int age;
    public int getAge(){
        return age;
    }
    public void setAge (int a){
        age = a;
    }
}
